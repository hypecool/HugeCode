export const INTERACTIVE_FAMILIES = new Set(
  Object.entries(COMPONENT_FAMILY_REGISTRY)
    .filter(([, definition]) => definition.interactive)
    .map(([familyName]) => familyName)
);

export function normalizeComponentFamilyName(value) {
  return String(value ?? "").replace(/\s+/gu, "");
}

export function getFamilyDefinition(familyName) {
  return (
    COMPONENT_FAMILY_REGISTRY[familyName] ??
    COMPONENT_FAMILY_REGISTRY[normalizeComponentFamilyName(familyName)] ??
    COMPONENT_FAMILY_REGISTRY.LocalPattern
  );
}

function fallbackPrimitiveFamily(component, node) {
  if (component.classification === "primitive" || node?.role === "layout") {
    if (/text|label|title|caption/iu.test(component.name)) {
      return "Text";
    }
    if (/stack|column|vertical/iu.test(component.name) || node?.layout?.mode === "vertical") {
      return "Stack";
    }
    if (/inline|row|horizontal/iu.test(component.name) || node?.layout?.mode === "horizontal") {
      return "Inline";
    }
    if (/surface|panel/iu.test(component.name)) {
      return "Surface";
    }
    return "Box";
  }

  if (/card/iu.test(component.name)) {
    return "Card";
  }

  return "LocalPattern";
}

export function inferFamilyFromComponent(component, node) {
  const name = String(component?.name ?? "");

  for (const [familyName, definition] of Object.entries(COMPONENT_FAMILY_REGISTRY)) {
    if (familyName === "LocalPattern") {
      continue;
    }
    for (const matcher of definition.matchers) {
      if (matcher.test(name)) {
        return {
          familyName,
          confidence: 0.88,
          evidence: [`Matched family registry pattern ${matcher}`],
        };
      }
    }
  }

  const primitiveFamily = fallbackPrimitiveFamily(component, node);
  return {
    familyName: primitiveFamily,
    confidence: primitiveFamily === "LocalPattern" ? 0.52 : 0.68,
    evidence: [
      primitiveFamily === "LocalPattern"
        ? "No strong family match; keeping candidate local."
        : "Derived from component classification and layout/name heuristics.",
    ],
  };
}

export function deriveComponentKind(familyName, component) {
  if (component.classification === "page-pattern") {
    return "local-pattern";
  }
  return getFamilyDefinition(familyName).kind;
}

export function deriveSharedLevel(familyName, component) {
  const familyLevel = getFamilyDefinition(familyName).sharedLevel;
  if (component.shared) {
    return SUPPORTED_COMPONENT_SHARED_LEVELS.includes(familyLevel)
      ? familyLevel
      : "shared-component";
  }
  if (familyLevel === "shared-foundation" || familyLevel === "shared-component") {
    return "app-adapter";
  }
  return SUPPORTED_COMPONENT_SHARED_LEVELS.includes(familyLevel) ? familyLevel : "app-local";
}

export function deriveTargetLayer(sharedLevel) {
  const targetMap = {
    "shared-foundation": "design-system-primitive",
    "shared-component": "design-system-component",
    "app-adapter": "app-adapter",
    "app-local": "app-pattern",
    defer: "defer",
  };
  const value = targetMap[sharedLevel] ?? "defer";
  return SUPPORTED_GENERATION_TARGET_LAYERS.includes(value) ? value : "defer";
}

export function isInteractiveFamily(familyName) {
  return INTERACTIVE_FAMILIES.has(familyName);
}
